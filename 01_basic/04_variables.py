# Declaración y asignación de variables
my_name = 'Jhon Bairo'
age = 24

print('my name is', my_name, 'and my age is', age, 'years old')

# Reasignación de variables
my_name = 'Jhon'
print('my name now is', my_name)

# Type hinting
my_name: str = 'Jhon Bairo'
age: int = 24

print('my name type:', type(my_name))
print('my age type:', type(age))
